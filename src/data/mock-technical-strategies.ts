const spark = (seed: number): number[] => {
  const pts: number[] = [100];
  let v = 100;
  for (let i = 1; i < 24; i++) {
    const r = Math.sin(seed * i * 0.7) * 0.5 + 0.5;
    v = Math.max(40, v + (r - 0.35) * 6 + 1.8);
    pts.push(Math.round(v * 10) / 10);
  }
  return pts;
};

