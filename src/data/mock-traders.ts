const spark = (seed: number, trend: "up" | "volatile" | "down"): number[] => {
  const pts: number[] = [100];
  let v = 100;
  for (let i = 1; i < 24; i++) {
    const r = Math.sin(seed * i * 0.7) * 0.5 + 0.5;
    const bias = trend === "up" ? 2.5 : trend === "down" ? -2 : 0.5;
    v = Math.max(40, v + (r - 0.4) * 8 + bias);
    pts.push(Math.round(v * 10) / 10);
  }
  return pts;
};

